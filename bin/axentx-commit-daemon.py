#!/usr/bin/env python3
"""axentx commit daemon — picks items from commit-queue, writes a
markdown decision record into the project repo, commits, pushes, marks done.
"""
from __future__ import annotations
import os, sys, datetime, subprocess
from pathlib import Path
sys.path.insert(0, str(Path(__file__).parent))
from axentx_pipeline import (REPO_ROOT, log, pick_oldest, advance, fail,
                             daemon_loop)

POLL_SEC = 60
PROJECTS_ROOT = Path(os.environ.get("AXENTX_ROOT", "/opt/axentx"))


def render_decision_md(item: dict) -> str:
    out = ["# axentx-dev-bot decision",
           f"- id: `{item['id']}`",
           f"- project: {item.get('project')}",
           f"- focus: {item.get('focus')}",
           f"- created_at: {item.get('created_at')}",
           ""]
    for h in item.get("history", []):
        out.append(f"## {h.get('stage')} — {h.get('actor')} @ {h.get('at')}")
        out.append("")
        out.append(h.get("output", "")[:4000])
        out.append("")
    return "\n".join(out)


def do_one_commit() -> bool:
    picked = pick_oldest("commit")
    if not picked: return False
    src_path, item = picked
    project = item.get("project", "")
    repo = PROJECTS_ROOT / project
    if not repo.exists():
        fail(item, src_path, "commit", f"project repo missing: {repo}")
        return True

    # Drop the decision into the repo's .axentx-dev-bot/ folder
    target_dir = repo / ".axentx-dev-bot"
    target_dir.mkdir(exist_ok=True)
    target_md = target_dir / f"{item['id']}.md"
    target_md.write_text(render_decision_md(item))
    log("commit", f"▸ {item['id']} → wrote {target_md}")

    # git add + commit + push
    try:
        subprocess.run(["git", "-C", str(repo), "add",
                        str(target_md.relative_to(repo))],
                       check=True, capture_output=True)
        commit_msg = (f"axentx-dev-bot: {item.get('focus')} cycle "
                      f"{item['id'][:24]}\n\n"
                      f"Auto-generated by axentx pipeline (dev → review → qa).\n"
                      f"See .axentx-dev-bot/{target_md.name} for full record.")
        r = subprocess.run(["git", "-C", str(repo), "commit", "-m", commit_msg],
                           capture_output=True, text=True)
        if r.returncode != 0:
            log("commit", f"  commit: {(r.stdout+r.stderr)[:200]}")
        else:
            push = subprocess.run(["git", "-C", str(repo), "push", "origin", "HEAD"],
                                  capture_output=True, text=True, timeout=60)
            if push.returncode == 0:
                log("commit", f"✓ {item['id']} pushed to {project}")
            else:
                log("commit", f"  push: {(push.stdout+push.stderr)[:200]}")
    except subprocess.TimeoutExpired:
        log("commit", f"⏱ {item['id']} push TIMEOUT")
    except Exception as e:
        log("commit", f"✗ {item['id']}: {e}")

    # Always advance to done (don't reprocess)
    advance(item, src_path, "done", "commit", target_md.name)
    return True


if __name__ == "__main__":
    daemon_loop("commit", POLL_SEC, do_one_commit)
